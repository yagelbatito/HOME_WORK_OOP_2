/**
 * Interface for marine animals, providing a method to represent swimming behavior.
 * <p>
 * This interface should be implemented by any class that represents an animal
 * capable of swimming in a marine environment.
 * </p>
 */
public interface MarineAnimalInterface {

    /**
     * Defines the swimming behavior of a marine animal.
     * <p>
     * Implementing classes should provide the specific details of how the animal
     * swims.
     * </p>
     */
    void swim();
}

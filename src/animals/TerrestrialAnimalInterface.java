package animals;

/**
 * The `TerrestrialAnimalInterface` interface defines behaviors specific to terrestrial animals.
 * Terrestrial animals are those that primarily live on land and have specific movements related
 * to walking.
 * <p>
 * This interface provides a method to model walking behavior for terrestrial animals.
 * </p>
 */
public interface TerrestrialAnimalInterface {

    /**
     * Defines the walking behavior for terrestrial animals.
     * <p>
     * Implementing classes should provide the actual walking behavior.
     * </p>
     */
    void walk();
}
